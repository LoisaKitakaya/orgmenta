// Use bullet points.

// Ordered list - All to do (e.g. must do 1, then 2, then 3)
// Use numbers for this (1,2,3).

// 'Or' Optional lists (e.g. you can do a or b or c)
// Use letters for this (A,B,C)

// 'Andor' Multiple selectable (e.g. you can do any of these: i,ii,iii)
// Use numerals for this (i,ii,iii)

// Combos
// Use nested. 
